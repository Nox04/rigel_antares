/*
|--------------------------------------------------------------------------
| Mutation Types
|--------------------------------------------------------------------------
*/
export const SET_DATA = 'SET_DATA';
export const UNSET_DATA = 'UNSET_DATA';

/*
|--------------------------------------------------------------------------
| Initial State
|--------------------------------------------------------------------------
*/
const initialState = {
	data: null
};

/*
|--------------------------------------------------------------------------
| Mutations
|--------------------------------------------------------------------------
*/
const mutations = {
	[SET_DATA](state, payload) {
		state.data = payload.data;
  },
  [UNSET_DATA](state) {
		state.data = null;
	}
};

/*
|--------------------------------------------------------------------------
| Actions
|--------------------------------------------------------------------------
*/
const actions = {
	setData: (context, data) => {
		context.commit(SET_DATA, {data});
  },
  unsetData: (context) => {
		context.commit(UNSET_DATA);
	}
};

/*
|--------------------------------------------------------------------------
| Getters
|--------------------------------------------------------------------------
*/
const getters = {
	formData: (state) => {
		return state.data;
  }
};

/*
|--------------------------------------------------------------------------
| Export the module
|--------------------------------------------------------------------------
*/
export default {
	state: initialState,
	mutations,
	actions,
	getters
}
